# Exercise 9: If, Elif, and Else Exercise
# Given a number x print out the following:
# 1. If x is positive, print out 'x is positive'
# 2. If x is negative, print out 'x is negative'
# 3. If x is 0, print out 'x is 0'

x = 5

if x > 0:
    print("x is positive")
elif x < 0:
    print("x is negative")
else:
    print("x is 0")
