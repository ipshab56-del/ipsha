# Given the variable x, use the assignment shortcuts to modify its value with the following operations:
# 1. Multiply x by 3
# 2. Divide x by 2
# 3. Add 10 to x

x = 12
print(f"Initial x: {x}")

x *= 3
print(f"After multiply by 3: {x}")

x /= 2
print(f"After divide by 2: {x}")

x += 10
print(f"After add 10: {x}")
