# Exercise 7: Order of Operations Exercise
# Using parentheses change the order of operations in the following expressions to get the desired result.

# Example: Without parentheses
print(f"2 + 3 * 4 = {2 + 3 * 4}")  # 14

# With parentheses to change order
print(f"(2 + 3) * 4 = {(2 + 3) * 4}")  # 20

# More examples
print(f"2 * 3 ** 2 = {2 * 3 ** 2}")  # 18
print(f"(2 * 3) ** 2 = {(2 * 3) ** 2}")  # 36
