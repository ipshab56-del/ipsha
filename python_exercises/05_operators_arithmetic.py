# Exercise 5: Arithmetic Operators Exercise
# Given the variable x and the variable y which have been set for you, write the following:
# 1. Print the value of x multiplied by y
# 2. Print the value of the output of step 1 divided by 1.5
# 3. Print the value of the output of step 2 plus 20
# 4. Print the value of the output of step 3 minus 10

x = 10
y = 5

step1 = x * y
print(f"Step 1 (x * y): {step1}")

step2 = step1 / 1.5
print(f"Step 2 (step1 / 1.5): {step2}")

step3 = step2 + 20
print(f"Step 3 (step2 + 20): {step3}")

step4 = step3 - 10
print(f"Step 4 (step3 - 10): {step4}")
